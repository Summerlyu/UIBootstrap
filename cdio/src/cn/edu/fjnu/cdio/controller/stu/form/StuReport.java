package cn.edu.fjnu.cdio.controller.stu.form;

/**
 * @author 蔚强
 * 分析学生报表
 */
public class StuReport {

	public String getType() {
		return grade;
	}
	public void setType(String type) {
		this.grade = type;
	}
	public StuReport() {
	}
	public StuReport(String type, Long count) {
		this.grade = type;
		this.count = count;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	private String grade;
	private Long count;
}
