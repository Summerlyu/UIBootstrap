package cn.edu.fjnu.cdio.model.coa;

import java.sql.Date;

/**
 * @author 罗成蓬
 *
 */
public class TeachInfo {
	
	private Integer courseID;
	private String courseName;
	private String grade;
	private String unit;
	private Double ep;
	private Date classTime;
	private String version;
	private Integer pattern;
	private Integer stuAmount;
	
	
	public Integer getCourseID() {
		return courseID;
	}


	public void setCourseID(Integer courseID) {
		this.courseID = courseID;
	}


	public String getCourseName() {
		return courseName;
	}


	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}


	public String getGrade() {
		return grade;
	}


	public void setGrade(String grade) {
		this.grade = grade;
	}


	public String getUnit() {
		return unit;
	}


	public void setUnit(String unit) {
		this.unit = unit;
	}


	public Double getEp() {
		return ep;
	}


	public void setEp(Double ep) {
		this.ep = ep;
	}


	public Date getClassTime() {
		return classTime;
	}


	public void setClassTime(Date classTime) {
		this.classTime = classTime;
	}


	public String getVersion() {
		return version;
	}


	public void setVersion(String version) {
		this.version = version;
	}


	public int getPattern() {
		return pattern;
	}


	public void setPattern(Integer pattern) {
		this.pattern = pattern;
	}


	public int getStuAmount() {
		return stuAmount;
	}


	public void setStuAmount(Integer stuAmount) {
		this.stuAmount = stuAmount;
	}



	@Override
	public String toString() {
		return "Course [courseID=" + courseID + ", courseName=" + courseName
				+ ", grade=" + grade + ", unit=" + unit 
				+ ", ep=" + ep + ", classTime=" + classTime
				+ ", version=" + version + ", pattern=" + pattern
				+ ",stuAmount= "+stuAmount 
				+ "]";
	}

}
