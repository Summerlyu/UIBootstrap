package cn.edu.fjnu.cdio.model.grp;

import java.io.Serializable;
import java.util.Date;

import cn.edu.fjnu.cdio.model.common.User;

/**
 * 回复里的回复
 * 
 * @author Administrator
 * 
 */
@SuppressWarnings("serial")
public class ReplyReply implements Serializable{
	private Long replyReplyId;
	private String replyContent;
	private Date replyTime;
	private ReplyTopic replyTopic;
	private User user;
	
	

	public ReplyTopic getReplyTopic() {
		return replyTopic;
	}

	public void setReplyTopic(ReplyTopic replyTopic) {
		this.replyTopic = replyTopic;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Long getReplyReplyId() {
		return replyReplyId;
	}

	public void setReplyReplyId(Long replyReplyId) {
		this.replyReplyId = replyReplyId;
	}

	public String getReplyContent() {
		return replyContent;
	}

	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}

	public Date getReplyTime() {
		return replyTime;
	}

	public void setReplyTime(Date replyTime) {
		this.replyTime = replyTime;
	}


}
