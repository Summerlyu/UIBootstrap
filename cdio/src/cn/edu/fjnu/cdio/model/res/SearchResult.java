public class SearchResult {

	private String resId;
	private String title;
	private String content;
	private String uploadDate;
	private String resType;
	private String resHttppath;
	private String author;

	public String getResId() {
		return resId;
	}

	public void setResId(String resId) {
		this.resId = resId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getResType() {
		return resType;
	}

	public void setResType(String resType) {
		this.resType = resType;
	}

	public String getResHttppath() {
		return resHttppath;
	}

	public void setResHttppath(String resHttppath) {
		this.resHttppath = resHttppath;
	}

	public String getUploadDate() {
		return uploadDate;
	}

	public void setUploadDate(String uploadDate) {
		this.uploadDate = uploadDate;
	}
	
	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	@Override
	public String toString() {
		return "SearchResult [resId=" + resId + ", title=" + title
				+ ", content=" + content + ", uploadDate=" + uploadDate
				+ ", resType=" + resType + ", resHttppath=" + resHttppath + "]";
	}


}
